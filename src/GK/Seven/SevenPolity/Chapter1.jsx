export default function Chapter1App() {
  const [tab, setTab] = useState("notes");
  const [quizIdx, setQuizIdx] = useState(0);
  const [selected, setSelected] = useState(null);
  const [score, setScore] = useState(0);
  const [answers, setAnswers] = useState([]);
  const [quizDone, setQuizDone] = useState(false);
  const [filter, setFilter] = useState("all");
  const [gameIdx, setGameIdx] = useState(0);
  const [gameScore, setGameScore] = useState(0);
  const [gameStreak, setGameStreak] = useState(0);
  const [gameSelected, setGameSelected] = useState(null);
  const [gameDone, setGameDone] = useState(false);
  const [timeLeft, setTimeLeft] = useState(20);
  const [feedback, setFeedback] = useState(null);
  const [gameAnswers, setGameAnswers] = useState([]);

  const filteredQ = filter === "all" ? QUESTIONS : QUESTIONS.filter(q => q.tag === filter);

  useEffect(() => {
    if (tab !== "game" || gameDone || gameSelected !== null) return;
    if (timeLeft <= 0) {
      handleGameAnswer(-1);
      return;
    }
    const t = setTimeout(() => setTimeLeft(t => t - 1), 1000);
    return () => clearTimeout(t);
  }, [tab, timeLeft, gameDone, gameSelected]);

  const handleQuizAnswer = (i) => {
    if (selected !== null) return;
    setSelected(i);
    const correct = i === filteredQ[quizIdx].ans;
    if (correct) setScore(s => s + 1);
    setAnswers(a => [...a, { qid: filteredQ[quizIdx].id, correct, selected: i }]);
  };

  const nextQuiz = () => {
    if (quizIdx + 1 >= filteredQ.length) { setQuizDone(true); return; }
    setQuizIdx(q => q + 1);
    setSelected(null);
  };

  const resetQuiz = () => {
    setQuizIdx(0); setSelected(null); setScore(0); setAnswers([]); setQuizDone(false);
  };

  const handleGameAnswer = (i) => {
    if (gameSelected !== null) return;
    setGameSelected(i);
    const q = QUESTIONS[gameIdx];
    const correct = i === q.ans;
    setGameAnswers(a => [...a, correct]);
    if (correct) {
      setGameScore(s => s + Math.max(10, timeLeft * 5));
      setGameStreak(s => s + 1);
      setFeedback("✅");
    } else {
      setGameStreak(0);
      setFeedback("❌");
    }
    setTimeout(() => {
      setFeedback(null);
      if (gameIdx + 1 >= QUESTIONS.length) { setGameDone(true); return; }
      setGameIdx(g => g + 1);
      setGameSelected(null);
      setTimeLeft(20);
    }, 900);
  };

  const resetGame = () => {
    setGameIdx(0); setGameScore(0); setGameStreak(0);
    setGameSelected(null); setGameDone(false); setTimeLeft(20);
    setFeedback(null); setGameAnswers([]);
  };

  const pct = filteredQ.length > 0 ? Math.round((quizIdx / filteredQ.length) * 100) : 0;
  const scorePercent = filteredQ.length > 0 ? Math.round((score / filteredQ.length) * 100) : 0;
  const timerColor = timeLeft > 10 ? '#4CAF82' : timeLeft > 5 ? '#F0D080' : '#E05C6C';

  return (
    <>
      <style>{styles}</style>
      <div>
        <div className="hero">
          <div className="chapter-badge">8th Standard Civics • Chapter 1</div>
          <h1>சமத்துவம்</h1>
          <h2>Equality — TNPSC Study App</h2>
        </div>

        <div className="tabs">
          {[["notes","📚 Notes"],["quiz","🎯 Quiz"],["game","⚡ Quiz Game"]].map(([k,l])=>(
            <button key={k} className={`tab-btn${tab===k?' active':''}`} onClick={()=>{ setTab(k); if(k==='game'&&!gameDone){setTimeLeft(20);setGameSelected(null);} }}>
              {l}
            </button>
          ))}
        </div>

        <div className="app-container content">
          {tab === "notes" && (
            <div>
              <div className="section-title">📖 முழு பாட குறிப்புகள்</div>
              <div className="section-subtitle">All important points covered from the textbook</div>
              {NOTES_SECTIONS.map((sec, si) => (
                <div key={si} className="note-card">
                  <h3><span className="icon">{sec.icon}</span>{sec.title}</h3>
                  {sec.content.map((c, ci) => {
                    if (c.type === "para") return <p key={ci}>{c.text}</p>;
                    if (c.type === "quote") return <div key={ci} className="highlight-box"><strong>💬 </strong>{c.text}</div>;
                    if (c.type === "list") return <ul key={ci}>{c.items.map((it,ii)=><li key={ii}>{it}</li>)}</ul>;
                    if (c.type === "table") return (
                      <div style={{overflowX:'auto'}} key={ci}>
                        <table className="premium-table">
                          <thead><tr>{c.headers.map((h,hi)=><th key={hi}>{h}</th>)}</tr></thead>
                          <tbody>{c.rows.map((row,ri)=><tr key={ri}>{row.map((cell,ci2)=><td key={ci2}>{cell}</td>)}</tr>)}</tbody>
                        </table>
                      </div>
                    );
                    return null;
                  })}
                </div>
              ))}
            </div>
          )}

          {tab === "quiz" && (
            <div>
              {quizDone ? (
                <div className="result-page">
                  <div className="result-score-ring" style={{"--pct":`${scorePercent * 3.6}deg`}}>
                    <div className="result-score-inner">
                      <div className="result-score-num">{score}/{filteredQ.length}</div>
                      <div className="result-score-label">{scorePercent}%</div>
                    </div>
                  </div>
                  <div className="result-grade">{scorePercent >= 80 ? "🏆 Excellent!" : scorePercent >= 60 ? "👍 Good" : "📚 Keep Studying"}</div>
                  <div className="result-msg">சமத்துவம் Quiz முடிந்தது!</div>
                  <div className="result-stats">
                    <div className="stat-pill correct-pill">✅ Correct: {score}</div>
                    <div className="stat-pill wrong-pill">❌ Wrong: {filteredQ.length - score}</div>
                  </div>
                  <div style={{marginBottom:20}}>
                    {answers.map((a,i)=>(
                      <div key={i} style={{display:'flex',alignItems:'center',gap:8,marginBottom:6}}>
                        <span style={{color:a.correct?'#4CAF82':'#E05C6C',fontSize:16}}>{a.correct?'✅':'❌'}</span>
                        <span style={{fontSize:13,color:'var(--text-muted)'}}>{filteredQ.find(q=>q.id===a.qid)?.q.slice(0,60)}...</span>
                      </div>
                    ))}
                  </div>
                  <button className="nav-btn primary" onClick={resetQuiz}>🔄 Try Again</button>
                </div>
              ) : (
                <div>
                  <div style={{marginBottom:16}}>
                    <div className="filter-tabs">
                      {[["all","All"],["group1","Group 1"],["group2","Group 2"],["group4","Group 4"],["vao","VAO"]].map(([k,l])=>(
                        <button key={k} className={`filter-chip${filter===k?' active':''}`} onClick={()=>{setFilter(k);setQuizIdx(0);setSelected(null);setScore(0);setAnswers([]);setQuizDone(false);}}>
                          {l}
                        </button>
                      ))}
                    </div>
                    <div className="quiz-header">
                      <div className="q-counter">Q {quizIdx+1} / {filteredQ.length}</div>
                      <div className="progress-bar-wrap">
                        <div className="progress-bar-fill" style={{width:`${pct}%`}}/>
                      </div>
                      <div style={{fontSize:13,color:'var(--gold)',fontWeight:700}}>Score: {score}</div>
                    </div>
                  </div>
                  {filteredQ.length === 0 ? (
                    <div style={{textAlign:'center',color:'var(--text-muted)',padding:'40px 0'}}>No questions for this filter.</div>
                  ) : (
                    <div className="question-card">
                      <div className="q-number">QUESTION {quizIdx + 1}</div>
                      <div className="tnpsc-badge" style={{background:'rgba(201,168,76,0.15)',color:'var(--gold)',border:'1px solid rgba(201,168,76,0.3)',display:'inline-block',fontSize:10,padding:'2px 8px',borderRadius:10,marginBottom:8,textTransform:'uppercase',letterSpacing:1}}>{filteredQ[quizIdx].group}</div>
                      <div className="q-text">{filteredQ[quizIdx].q}</div>
                      <div className="options-grid">
                        {filteredQ[quizIdx].opts.map((opt,i)=>(
                          <button key={i} className={`option-btn${selected===null?'': i===filteredQ[quizIdx].ans?' correct': i===selected?' wrong':' reveal'}`}
                            onClick={()=>handleQuizAnswer(i)} disabled={selected!==null}>
                            <span style={{color:'var(--gold)',fontWeight:700,marginRight:6}}>{String.fromCharCode(65+i)}.</span>{opt}
                          </button>
                        ))}
                      </div>
                      {selected !== null && (
                        <div className="explanation-box">
                          <strong>{selected === filteredQ[quizIdx].ans ? '✅ Correct!' : '❌ Wrong!'}</strong><br/>
                          <strong>Explanation: </strong>{filteredQ[quizIdx].exp}
                        </div>
                      )}
                      <div className="nav-row">
                        <div style={{fontSize:13,color:'var(--text-muted)'}}>Score: <span style={{color:'var(--gold)',fontWeight:700}}>{score}</span></div>
                        {selected !== null && (
                          <button className="nav-btn primary" onClick={nextQuiz}>
                            {quizIdx+1 >= filteredQ.length ? "See Result 🏆" : "Next →"}
                          </button>
                        )}
                      </div>
                    </div>
                  )}
                </div>
              )}
            </div>
          )}

          {tab === "game" && (
            <div className="game-container">
              {gameDone ? (
                <div className="result-page">
                  <div className="result-score-ring" style={{"--pct":`${Math.round((gameAnswers.filter(Boolean).length/QUESTIONS.length)*360)}deg`}}>
                    <div className="result-score-inner">
                      <div className="result-score-num">{gameScore}</div>
                      <div className="result-score-label">Points</div>
                    </div>
                  </div>
                  <div className="result-grade">⚡ Game Over!</div>
                  <div className="result-msg">{gameAnswers.filter(Boolean).length}/{QUESTIONS.length} correct</div>
                  <div className="result-stats">
                    <div className="stat-pill correct-pill">✅ {gameAnswers.filter(Boolean).length}</div>
                    <div className="stat-pill wrong-pill">❌ {gameAnswers.filter(v=>!v).length}</div>
                  </div>
                  <button className="nav-btn primary" onClick={resetGame}>🔄 Play Again</button>
                </div>
              ) : (
                <div>
                  {feedback && <div className="feedback-flash">{feedback}</div>}
                  <div className="score-display">
                    <div className="score-badge">⚡ {gameScore} pts</div>
                    <div className="streak-badge">🔥 Streak: {gameStreak}</div>
                    <div style={{fontSize:13,color:'var(--text-muted)'}}>Q {gameIdx+1}/{QUESTIONS.length}</div>
                  </div>
                  <div className="timer-bar">
                    <div className="timer-fill" style={{width:`${(timeLeft/20)*100}%`,background:timerColor}}/>
                  </div>
                  <div style={{textAlign:'right',fontSize:12,color:timerColor,marginBottom:12,fontWeight:700}}>⏱ {timeLeft}s</div>
                  <div className="question-card">
                    <div className="q-number">SPEED QUIZ • Q{gameIdx+1}</div>
                    <div className="q-text">{QUESTIONS[gameIdx].q}</div>
                    <div className="options-grid">
                      {QUESTIONS[gameIdx].opts.map((opt,i)=>(
                        <button key={i} className={`option-btn${gameSelected===null?'': i===QUESTIONS[gameIdx].ans?' correct': i===gameSelected?' wrong':' reveal'}`}
                          onClick={()=>handleGameAnswer(i)} disabled={gameSelected!==null}>
                          <span style={{color:'var(--gold)',fontWeight:700,marginRight:6}}>{String.fromCharCode(65+i)}.</span>{opt}
                        </button>
                      ))}
                    </div>
                    {gameSelected !== null && (
                      <div className="explanation-box" style={{fontSize:12}}>
                        {gameSelected === QUESTIONS[gameIdx].ans ? '✅ Correct! +' + Math.max(10,timeLeft*5) + ' pts' : '❌ Wrong!'} — {QUESTIONS[gameIdx].exp.slice(0,120)}...
                      </div>
                    )}
                  </div>
                </div>
              )}
            </div>
          )}
        </div>
      </div>
    </>
  );
}
