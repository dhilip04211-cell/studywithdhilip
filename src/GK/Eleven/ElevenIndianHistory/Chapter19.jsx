  const [selected, setSelected] = useState(null);
  const [timeLeft, setTimeLeft] = useState(30);
  const [showFeedback, setShowFeedback] = useState(null);
  const [timerActive, setTimerActive] = useState(false);
  const timerRef = useRef(null);

  const shuffle = (arr) => [...arr].sort(() => Math.random() - 0.5);

  const startGame = () => {
    const shuffled = shuffle(quizQuestions).slice(0, 20);
    setQuestions(shuffled);
    setCurrent(0); setScore(0); setLives(3); setStreak(0); setMaxStreak(0);
    setSelected(null); setTimeLeft(30); setShowFeedback(null);
    setGameState("playing"); setTimerActive(true);
  };

  useEffect(() => {
    if (timerActive && gameState === "playing") {
      timerRef.current = setInterval(() => {
        setTimeLeft(t => {
          if (t <= 1) {
            clearInterval(timerRef.current);
            setTimerActive(false);
            setLives(l => {
              const newL = l - 1;
              if (newL <= 0) setTimeout(() => setGameState("gameover"), 500);
              return newL;
            });
            setStreak(0);
            setShowFeedback("timeout");
            return 0;
          }
          return t - 1;
        });
      }, 1000);
    }
    return () => clearInterval(timerRef.current);
  }, [timerActive, gameState, current]);

