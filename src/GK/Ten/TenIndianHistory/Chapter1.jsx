function QuizSection({ questions, title, subtitle, sourceNote }) {
  const [current, setCurrent] = useState(0);
  const [selected, setSelected] = useState(null);
  const [answers, setAnswers] = useState([]);
  const [showResult, setShowResult] = useState(false);
  const [shuffled, setShuffled] = useState(() =>
    [...questions].sort(() => Math.random() - 0.5).slice(0, Math.min(questions.length, 50))
  );

  const restart = () => {
    setCurrent(0);
    setSelected(null);
    setAnswers([]);
    setShowResult(false);
    setShuffled([...questions].sort(() => Math.random() - 0.5).slice(0, Math.min(questions.length, 50)));
  };

  if (showResult) {
    const score = answers.filter(a => a.correct).length;
    const pct = Math.round((score / shuffled.length) * 100);
    return (
      <div style={{ padding: "24px 16px", maxWidth: 700, margin: "0 auto" }}>
        <div style={{ background: "linear-gradient(135deg,#0a0a0a,#1a1a2e)", borderRadius: 20, padding: 32, color: "#fff", textAlign: "center", border: "1px solid #333" }}>
          <div style={{ fontSize: 64, marginBottom: 12 }}>{pct >= 80 ? "🏆" : pct >= 60 ? "👍" : "📚"}</div>
          <h2 style={{ fontFamily: "'Noto Serif Tamil','serif'", fontSize: 24, marginBottom: 8 }}>முடிவுகள்</h2>
          <div style={{ fontSize: 56, fontWeight: 800, color: pct >= 80 ? "#4ade80" : pct >= 60 ? "#fbbf24" : "#f87171", marginBottom: 8 }}>{pct}%</div>
          <p style={{ color: "#ccc", marginBottom: 24 }}>{shuffled.length} கேள்விகளில் {score} சரி</p>
          <div style={{ display: "grid", gridTemplateColumns: "1fr 1fr", gap: 12, marginBottom: 24 }}>
            {answers.map((a, i) => (
              <div key={i} style={{ background: a.correct ? "#052e16" : "#2d0a0a", borderRadius: 8, padding: "8px 12px", fontSize: 12, textAlign: "left", borderLeft: `3px solid ${a.correct ? "#4ade80" : "#f87171"}` }}>
                <div style={{ color: "#aaa", marginBottom: 4 }}>Q{i + 1}: {shuffled[i].q.slice(0, 50)}...</div>
                <div style={{ color: a.correct ? "#4ade80" : "#f87171" }}>{a.correct ? "✓ சரி" : `✗ தவறு`}</div>
                {!a.correct && <div style={{ color: "#ccc", fontSize: 11 }}>சரி: {shuffled[i].opts[shuffled[i].ans]}</div>}
              </div>
            ))}
          </div>
          <button onClick={restart} style={{ background: "linear-gradient(135deg,#FF6B35,#ff9a6c)", color: "#fff", border: "none", borderRadius: 12, padding: "14px 32px", fontSize: 16, cursor: "pointer", fontFamily: "'Noto Serif Tamil',serif" }}>மீண்டும் முயற்சி ↺</button>
        </div>
      </div>
    );
  }

  const q = shuffled[current];
  return (
    <div style={{ padding: "16px", maxWidth: 700, margin: "0 auto" }}>
      {sourceNote && <div style={{ background: "#1a1a2e", borderRadius: 8, padding: "8px 14px", marginBottom: 12, fontSize: 12, color: "#888", borderLeft: "3px solid #FF6B35" }}>📌 {sourceNote}</div>}
      <div style={{ background: "linear-gradient(135deg,#0a0a0a,#1a1a2e)", borderRadius: 20, padding: 24, color: "#fff", border: "1px solid #222" }}>
        <div style={{ display: "flex", justifyContent: "space-between", marginBottom: 16 }}>
          <span style={{ color: "#FF6B35", fontWeight: 700, fontSize: 13 }}>கேள்வி {current + 1}/{shuffled.length}</span>
          <div style={{ background: "#222", borderRadius: 20, padding: "4px 14px", fontSize: 12, color: "#aaa" }}>
            ✓ {answers.filter(a => a.correct).length} | ✗ {answers.filter(a => !a.correct).length}
          </div>
        </div>
        <div style={{ background: "#1a1a2e", borderRadius: 12, padding: "4px 0", marginBottom: 16 }}>
          <div style={{ height: 4, background: "linear-gradient(to right,#FF6B35,#ff9a6c)", borderRadius: 4, width: `${((current) / shuffled.length) * 100}%`, transition: "width 0.4s" }} />
        </div>
        <p style={{ fontFamily: "'Noto Serif Tamil','Noto Sans Tamil',serif", fontSize: 17, lineHeight: 1.7, marginBottom: 20, color: "#f0f0f0" }}>{q.q}</p>
        <div style={{ display: "flex", flexDirection: "column", gap: 10 }}>
          {q.opts.map((opt, i) => {
            let bg = "#111";
            let border = "1px solid #333";
            let col = "#eee";
            if (selected !== null) {
              if (i === q.ans) { bg = "#052e16"; border = "2px solid #4ade80"; col = "#4ade80"; }
              else if (i === selected && i !== q.ans) { bg = "#2d0a0a"; border = "2px solid #f87171"; col = "#f87171"; }
            } else if (selected === null) {
              bg = "#111";
            }
            return (
              <button key={i} onClick={() => {
                if (selected !== null) return;
                setSelected(i);
                setAnswers(prev => [...prev, { correct: i === q.ans }]);
              }} style={{ background: bg, border, borderRadius: 10, padding: "12px 16px", color: col, cursor: selected !== null ? "default" : "pointer", textAlign: "left", fontFamily: "'Noto Serif Tamil','Noto Sans Tamil',sans-serif", fontSize: 14, lineHeight: 1.5, transition: "all 0.2s" }}>
                <span style={{ fontWeight: 700, marginRight: 10, color: "#FF6B35" }}>{String.fromCharCode(65 + i)}.</span>{opt}
              </button>
            );
          })}
        </div>
        {selected !== null && (
          <div style={{ marginTop: 16, background: selected === q.ans ? "#052e16" : "#1a0a0a", border: `1px solid ${selected === q.ans ? "#4ade80" : "#f87171"}`, borderRadius: 10, padding: 14 }}>
            <div style={{ color: selected === q.ans ? "#4ade80" : "#f87171", fontWeight: 700, marginBottom: 6 }}>{selected === q.ans ? "✅ சரியான விடை!" : "❌ தவறான விடை"}</div>
            <p style={{ color: "#ccc", fontSize: 13, lineHeight: 1.6, fontFamily: "'Noto Serif Tamil','Noto Sans Tamil',sans-serif" }}>{q.ex}</p>
            {q.source && <div style={{ marginTop: 8, fontSize: 11, color: "#666" }}>📚 ஆதாரம்: {q.source}</div>}
            <button onClick={() => { if (current + 1 >= shuffled.length) { setShowResult(true); } else { setCurrent(c => c + 1); setSelected(null); } }} style={{ marginTop: 12, background: "linear-gradient(135deg,#FF6B35,#ff9a6c)", color: "#fff", border: "none", borderRadius: 10, padding: "10px 24px", cursor: "pointer", fontSize: 14, fontFamily: "'Noto Serif Tamil',serif" }}>
              {current + 1 >= shuffled.length ? "முடிவு காண்க 🏁" : "அடுத்த கேள்வி →"}
            </button>
          </div>
        )}
      </div>
    </div>
  );
}

