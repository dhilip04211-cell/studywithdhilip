function PrevYearTab() {
  const [filter, setFilter] = useState("அனைத்தும்");
  const [qIdx, setPrevIdx] = useState(null);
  const [selected, setSelected] = useState(null);
  const [showExp, setShowExp] = useState(false);
  const [score, setScore] = useState(0);
  const [attempted, setAttempted] = useState(0);
  const [answered, setAnswered] = useState({});

  const sources = ["அனைத்தும்", "TNPSC Group 1", "TNPSC Group 2", "TNPSC Group 2A", "TNPSC Group 4", "TNPSC Group 4 VAO"];
  const filtered = filter === "அனைத்தும்" ? prevYearQuestions : prevYearQuestions.filter(q => q.source === filter);

  const handleSelect = (qI, optI) => {
    if (answered[qI] !== undefined) return;
    const correct = optI === filtered[qI].ans;
    setAnswered(a => ({ ...a, [qI]: optI }));
    if (correct) setScore(s => s + 1);
    setAttempted(a => a + 1);
  };

  return (
    <div>
      <div style={{ background: "linear-gradient(90deg,#065f46,#047857)", borderRadius: 12, padding: 12, marginBottom: 14, color: "#fff" }}>
        <div style={{ fontSize: "0.75rem", opacity: 0.85, marginBottom: 4 }}>📌 குறிப்பு: கீழ்கண்ட கேள்விகள் TNPSC தொடர்பான ஆதாரங்களிலிருந்து சேகரிக்கப்பட்டவை</div>
        <div style={{ fontWeight: 700, fontSize: "0.88rem" }}>மொத்தம் {prevYearQuestions.length} கேள்விகள் | முயற்சித்தவை: {attempted} | சரி: {score}</div>
      </div>

      {/* Filter */}
      <div style={{ display: "flex", overflowX: "auto", gap: 6, marginBottom: 14, WebkitOverflowScrolling: "touch", paddingBottom: 4 }}>
        {sources.map(s => (
          <button key={s} onClick={() => { setFilter(s); setAnswered({}); setScore(0); setAttempted(0); }} style={{
            flexShrink: 0, padding: "5px 12px", borderRadius: 20, border: "none", cursor: "pointer",
            fontWeight: 600, fontSize: "0.7rem",
            background: filter === s ? "#065f46" : "#e2e8f0",
            color: filter === s ? "#fff" : "#374151",
          }}>{s}</button>
        ))}
      </div>

      <div style={{ fontSize: "0.78rem", color: "#64748b", marginBottom: 10 }}>மொத்தம் {filtered.length} கேள்விகள்</div>

      {filtered.map((q, qI) => {
        const userAns = answered[qI];
        return (
          <div key={qI} style={{ background: "#fff", border: "1px solid #e2e8f0", borderRadius: 12, padding: 12, marginBottom: 14, boxShadow: "0 1px 4px rgba(0,0,0,0.06)" }}>
            <div style={{ display: "flex", justifyContent: "space-between", alignItems: "center", marginBottom: 6 }}>
              <span style={{ fontSize: "0.68rem", fontWeight: 700, color: "#065f46", background: "#dcfce7", padding: "2px 8px", borderRadius: 10 }}>{q.source}</span>
              <span style={{ fontSize: "0.68rem", color: "#94a3b8", fontWeight: 600 }}>{q.year}</span>
            </div>
            <div style={{ fontWeight: 700, color: "#1a3a5c", fontSize: "0.84rem", lineHeight: 1.6, marginBottom: 10 }}>
              <span style={{ color: "#64748b", fontWeight: 800 }}>{qI + 1}. </span>{q.q}
            </div>
            {q.opts.map((opt, i) => {
              let bg = "#f8fafc", border = "#e2e8f0", color = "#374151";
              if (userAns !== undefined) {
                if (i === q.ans) { bg = "#f0fdf4"; border = "#16a34a"; color = "#15803d"; }
                else if (i === userAns && i !== q.ans) { bg = "#fef2f2"; border = "#dc2626"; color = "#dc2626"; }
              }
              return (
                <button key={i} onClick={() => handleSelect(qI, i)} style={{
                  display: "flex", gap: 8, alignItems: "center", width: "100%", textAlign: "left",
                  background: bg, border: `1.5px solid ${border}`, borderRadius: 8,
                  padding: "7px 10px", marginBottom: 6, cursor: userAns !== undefined ? "default" : "pointer",
                  transition: "all 0.2s", color,
                }}>
                  <span style={{ fontSize: "0.7rem", fontWeight: 700, color: color, minWidth: 14 }}>{["அ", "ஆ", "இ", "ஈ"][i]})</span>
                  <span style={{ fontSize: "0.79rem" }}>{opt}</span>
                  {userAns !== undefined && i === q.ans && <span style={{ marginLeft: "auto", fontSize: "0.85rem" }}>✅</span>}
                  {userAns !== undefined && i === userAns && i !== q.ans && <span style={{ marginLeft: "auto", fontSize: "0.85rem" }}>❌</span>}
                </button>
              );
            })}
            {userAns !== undefined && (
              <div style={{ background: "#fffbeb", border: "1px solid #fcd34d", borderRadius: 8, padding: 8, marginTop: 6 }}>
                <div style={{ fontSize: "0.75rem", color: "#374151" }}><strong style={{ color: "#b45309" }}>💡 விளக்கம்: </strong>{q.exp}</div>
              </div>
            )}
          </div>
        );
      })}
    </div>
  );
}

