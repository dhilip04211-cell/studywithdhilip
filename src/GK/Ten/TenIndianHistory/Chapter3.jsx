// ── QUIZ COMPONENT ───────────────────────────────────────────────────────────

const QuizTab = ({ questions, title }) => {
  const [current, setCurrent] = useState(0);
  const [selected, setSelected] = useState(null);
  const [answered, setAnswered] = useState([]);
  const [showResult, setShowResult] = useState(false);
  const [showExplanation, setShowExplanation] = useState(false);
  const [shuffled] = useState(() => [...questions].sort(() => Math.random() - 0.5));

  const q = shuffled[current];
  const total = shuffled.length;
  const score = answered.filter(a => a.correct).length;

  const handleAnswer = (idx) => {
    if (selected !== null) return;
    setSelected(idx);
    setAnswered(prev => [...prev, { correct: idx === q.ans }]);
    setShowExplanation(true);
  };

  const next = () => {
    if (current < total - 1) {
      setCurrent(c => c + 1);
      setSelected(null);
      setShowExplanation(false);
    } else {
      setShowResult(true);
    }
  };

  const restart = () => {
    setCurrent(0);
    setSelected(null);
    setAnswered([]);
    setShowResult(false);
    setShowExplanation(false);
  };

  if (showResult) {
    const pct = Math.round((score / total) * 100);
    return (
      <div style={{ padding: "20px" }}>
        <div style={{ background: `linear-gradient(135deg, #1e1b4b, #0f172a)`, borderRadius: "16px", padding: "24px", textAlign: "center", border: `1px solid ${THEME.accent3}44` }}>
          <div style={{ fontSize: "48px", marginBottom: "8px" }}>
            {pct >= 80 ? "🏆" : pct >= 60 ? "🌟" : pct >= 40 ? "💪" : "📚"}
          </div>
          <div style={{ fontSize: "36px", fontWeight: "800", color: pct >= 60 ? THEME.success : THEME.danger }}>{pct}%</div>
          <div style={{ fontSize: "14px", color: THEME.muted, marginBottom: "16px" }}>{score} / {total} சரியான விடைகள்</div>
          <div style={{ fontSize: "13px", color: THEME.text, marginBottom: "20px", lineHeight: "1.6" }}>
            {pct >= 80 ? "மிகவும் சிறப்பு! தொடர்ந்து படியுங்கள்! 🎉" : pct >= 60 ? "நல்ல முயற்சி! இன்னும் கொஞ்சம் படியுங்கள்! 👍" : "தவறான பகுதிகளை மீண்டும் படியுங்கள்! 📖"}
          </div>
          {answered.map((a, i) => (
            <div key={i} style={{ display: "flex", alignItems: "center", gap: "8px", padding: "6px 10px", background: a.correct ? "#22c55e11" : "#ef444411", borderRadius: "6px", marginBottom: "4px" }}>
              <span>{a.correct ? "✅" : "❌"}</span>
              <span style={{ fontSize: "11px", color: THEME.muted }}>வினா {i + 1}</span>
            </div>
          ))}
          <button onClick={restart} style={{ marginTop: "16px", background: THEME.accent, color: "#fff", border: "none", borderRadius: "10px", padding: "12px 28px", fontSize: "14px", fontWeight: "700", cursor: "pointer", width: "100%" }}>
            மீண்டும் முயற்சி செய்
          </button>
        </div>
      </div>
    );
  }

  return (
    <div style={{ padding: "16px" }}>
      {/* Progress */}
      <div style={{ marginBottom: "16px" }}>
        <div style={{ display: "flex", justifyContent: "space-between", fontSize: "11px", color: THEME.muted, marginBottom: "6px" }}>
          <span>வினா {current + 1} / {total}</span>
          <span style={{ color: THEME.success }}>✅ {score} சரி</span>
        </div>
        <div style={{ height: "4px", background: THEME.border, borderRadius: "4px" }}>
          <div style={{ height: "100%", width: `${((current) / total) * 100}%`, background: `linear-gradient(90deg, ${THEME.accent}, ${THEME.accent2})`, borderRadius: "4px", transition: "width 0.3s" }} />
        </div>
      </div>

      {/* Question */}
      <div style={{ background: "#111827", borderRadius: "12px", padding: "16px", marginBottom: "14px", border: `1px solid ${THEME.border}` }}>
        {q.year && <div style={{ ...styles.badge(THEME.accent3), marginBottom: "8px" }}>📋 {q.year}</div>}
        <div style={{ fontSize: "14px", color: THEME.text, lineHeight: "1.7", fontWeight: "600" }}>{q.q}</div>
      </div>

      {/* Options */}
      {q.opts.map((opt, i) => {
        const isSelected = selected === i;
        const isCorrect = i === q.ans;
        let bg = "#0f172a";
        let border = THEME.border;
        let textCol = THEME.text;
        if (selected !== null) {
          if (isCorrect) { bg = "#22c55e22"; border = THEME.success; textCol = THEME.success; }
          else if (isSelected) { bg = "#ef444422"; border = THEME.danger; textCol = THEME.danger; }
        }
        return (
          <button key={i} onClick={() => handleAnswer(i)} style={{
            display: "block", width: "100%", textAlign: "left", padding: "12px 14px",
            background: bg, border: `1px solid ${border}`, borderRadius: "10px",
            color: textCol, fontSize: "13px", cursor: selected !== null ? "default" : "pointer",
            marginBottom: "8px", transition: "all 0.2s", lineHeight: "1.5",
          }}>
            <span style={{ fontWeight: "700", marginRight: "8px" }}>{String.fromCharCode(65 + i)}.</span>{opt}
            {selected !== null && isCorrect && " ✅"}
            {selected !== null && isSelected && !isCorrect && " ❌"}
          </button>
        );
      })}

      {/* Explanation */}
      {showExplanation && (
        <div style={{ background: "#0f172a", border: `1px solid ${THEME.accent2}44`, borderRadius: "10px", padding: "12px", marginBottom: "12px" }}>
          <div style={{ fontSize: "11px", fontWeight: "700", color: THEME.accent2, marginBottom: "6px" }}>💡 விளக்கம்</div>
          <div style={{ fontSize: "12px", color: THEME.text, lineHeight: "1.7" }}>{q.exp}</div>
          {q.source && <div style={{ fontSize: "10px", color: THEME.muted, marginTop: "6px" }}>📚 ஆதாரம்: {q.source}</div>}
        </div>
      )}

      {selected !== null && (
        <button onClick={next} style={{
          width: "100%", background: `linear-gradient(135deg, ${THEME.accent}, #f97316)`,
          color: "#fff", border: "none", borderRadius: "10px", padding: "14px",
          fontSize: "14px", fontWeight: "700", cursor: "pointer",
        }}>
          {current < total - 1 ? "அடுத்த வினா →" : "முடிவு காண"}
        </button>
      )}
    </div>
  );
};

