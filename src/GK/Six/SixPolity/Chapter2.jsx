function QuizGame() {
  const GAME_TIME = 20;
  const [phase, setPhase] = useState("start");
  const [pool, setPool] = useState([]);
  const [idx, setIdx] = useState(0);
  const [score, setScore] = useState(0);
  const [streak, setStreak] = useState(0);
  const [maxStreak, setMaxStreak] = useState(0);
  const [lives, setLives] = useState(3);
  const [timeLeft, setTimeLeft] = useState(GAME_TIME);
  const [selected, setSelected] = useState(null);
  const [flash, setFlash] = useState(null);
  const timerRef = useRef(null);

  const shuffle = (arr) => [...arr].sort(() => Math.random() - 0.5);

  const startGame = () => {
    const p = shuffle(QUESTIONS).slice(0, 20);
    setPool(p); setIdx(0); setScore(0); setStreak(0); setMaxStreak(0); setLives(3); setTimeLeft(GAME_TIME); setSelected(null); setFlash(null); setPhase("game");
  };

  useEffect(() => {
    if (phase !== "game" || selected !== null) return;
    timerRef.current = setInterval(() => {
      setTimeLeft(t => {
        if (t <= 1) {
          clearInterval(timerRef.current);
          handleAnswer(-1);
          return 0;
        }
        return t - 1;
      });
    }, 1000);
    return () => clearInterval(timerRef.current);
  }, [phase, idx, selected]);

  const handleAnswer = (i) => {
    clearInterval(timerRef.current);
    setSelected(i);
    const q = pool[idx];
    const correct = i === q.ans;
    if (correct) {
      const bonus = timeLeft > 15 ? 3 : timeLeft > 8 ? 2 : 1;
      const newStreak = streak + 1;
      setStreak(newStreak);
      setMaxStreak(ms => Math.max(ms, newStreak));
      setScore(s => s + (10 * bonus) + (newStreak > 2 ? newStreak * 2 : 0));
      setFlash("correct");
    } else {
      setStreak(0);
      const newLives = lives - 1;
      setLives(newLives);
      setFlash("wrong");
      if (newLives <= 0) { setTimeout(() => setPhase("end"), 1200); return; }
    }
    setTimeout(() => {
      setFlash(null);
      setSelected(null);
      setTimeLeft(GAME_TIME);
      if (idx + 1 >= pool.length) { setPhase("end"); return; }
      setIdx(n => n + 1);
    }, 1200);
  };

  if (phase === "start") {
    return (
      <div style={S.container}>
        <div style={{ ...S.card, textAlign: "center", padding: 36 }}>
          <div style={{ fontSize: 48, marginBottom: 16 }}>🎮</div>
          <div style={{ fontSize: 22, fontWeight: 700, color: "#60a5fa", marginBottom: 8 }}>வினாவிளையாட்டு</div>
          <div style={{ fontSize: 14, color: "#94a3b8", marginBottom: 24, lineHeight: 1.7 }}>
            20 வினாக்கள் · ஒவ்வொன்றுக்கும் {GAME_TIME} விநாடிகள்<br />
            3 உயிர்கள் · வேக பதில் = அதிக மதிப்பெண்
          </div>
          <div style={{ display: "grid", gridTemplateColumns: "1fr 1fr 1fr", gap: 12, marginBottom: 24 }}>
            {[["⚡", "வேகம்", "அதிக மதிப்பெண்"], ["🔥", "தொடர்ச்சி", "Streak பதிப்பு"], ["❤️", "3 உயிர்கள்", "கவனமாக!"]].map(([e, t, s]) => (
              <div key={t} style={{ padding: 12, background: "rgba(255,255,255,0.04)", borderRadius: 8, border: "1px solid rgba(255,255,255,0.08)" }}>
                <div style={{ fontSize: 24 }}>{e}</div>
                <div style={{ fontSize: 12, fontWeight: 700, color: "#60a5fa", marginTop: 4 }}>{t}</div>
                <div style={{ fontSize: 11, color: "#64748b" }}>{s}</div>
              </div>
            ))}
          </div>
          <button onClick={startGame} style={{ padding: "14px 40px", background: "linear-gradient(135deg, #2563eb, #059669)", color: "#fff", border: "none", borderRadius: 10, cursor: "pointer", fontWeight: 700, fontSize: 16 }}>விளையாட்டு தொடங்கு!</button>
        </div>
      </div>
    );
  }

  if (phase === "end") {
    const pct = Math.round((score / (pool.length * 30)) * 100);
    return (
      <div style={S.container}>
        <div style={{ ...S.card, textAlign: "center", padding: 32 }}>
          <div style={{ fontSize: 52 }}>{score > 200 ? "🏆" : score > 100 ? "⭐" : "🎯"}</div>
          <div style={{ fontSize: 32, fontWeight: 800, color: "#fbbf24", marginTop: 8 }}>{score} pts</div>
          <div style={{ fontSize: 14, color: "#94a3b8", marginBottom: 20 }}>விளையாட்டு முடிந்தது!</div>
          <div style={{ overflowX: "auto" }}>
            <table style={S.table}>
              <thead><tr><th style={S.th}>தலைப்பு</th><th style={S.th}>மதிப்பு</th></tr></thead>
              <tbody>
                {[["மொத்த மதிப்பெண்", `${score} pts`], ["அதிகபட்ச Streak", `${maxStreak} 🔥`], ["மீதமுள்ள உயிர்கள்", `${lives} ❤️`], ["பதிலளித்த கேள்விகள்", `${idx + 1}/${pool.length}`]].map(([k, v], i) => (
                  <tr key={k} style={i % 2 === 0 ? S.trEven : S.trOdd}>
                    <td style={S.td}>{k}</td>
                    <td style={{ ...S.td, fontWeight: 700, color: "#60a5fa" }}>{v}</td>
                  </tr>
                ))}
              </tbody>
            </table>
          </div>
          <button onClick={() => setPhase("start")} style={{ marginTop: 20, padding: "12px 32px", background: "linear-gradient(135deg, #2563eb, #059669)", color: "#fff", border: "none", borderRadius: 8, cursor: "pointer", fontWeight: 700 }}>மீண்டும் விளையாடு</button>
        </div>
      </div>
    );
  }

  const q = pool[idx];
  const timerPct = (timeLeft / GAME_TIME) * 100;
  const timerColor = timeLeft > 10 ? "#34d399" : timeLeft > 5 ? "#fbbf24" : "#f87171";

  return (
    <div style={S.container}>
      {/* HUD */}
      <div style={{ display: "grid", gridTemplateColumns: "1fr 1fr 1fr 1fr", gap: 8, marginBottom: 14 }}>
        {[
          { label: "மதிப்பெண்", value: score, icon: "⭐", color: "#fbbf24" },
          { label: "Streak", value: `${streak}🔥`, icon: "", color: "#f97316" },
          { label: "உயிர்கள்", value: "❤️".repeat(lives), icon: "", color: "#f87171" },
          { label: "கேள்வி", value: `${idx + 1}/20`, icon: "", color: "#60a5fa" },
        ].map(item => (
          <div key={item.label} style={{ background: "rgba(255,255,255,0.04)", border: "1px solid rgba(255,255,255,0.08)", borderRadius: 8, padding: "8px", textAlign: "center" }}>
            <div style={{ fontSize: "clamp(14px, 3vw, 18px)", fontWeight: 700, color: item.color }}>{item.value}</div>
            <div style={{ fontSize: 10, color: "#64748b" }}>{item.label}</div>
          </div>
        ))}
      </div>
      {/* Timer */}
      <div style={{ marginBottom: 14 }}>
        <div style={{ display: "flex", justifyContent: "space-between", marginBottom: 4 }}>
          <span style={{ fontSize: 12, color: "#64748b" }}>நேர வரம்பு</span>
          <span style={{ fontSize: 13, fontWeight: 700, color: timerColor }}>{timeLeft}s</span>
        </div>
        <div style={{ height: 6, background: "rgba(255,255,255,0.08)", borderRadius: 3, overflow: "hidden" }}>
          <div style={{ height: "100%", width: `${timerPct}%`, background: timerColor, transition: "width 1s linear, background 0.3s" }} />
        </div>
      </div>
      {/* Flash */}
      {flash && (
        <div style={{ padding: "10px", borderRadius: 8, textAlign: "center", marginBottom: 12, background: flash === "correct" ? "rgba(52,211,153,0.2)" : "rgba(248,113,113,0.2)", border: `1px solid ${flash === "correct" ? "#34d399" : "#f87171"}`, fontSize: 14, fontWeight: 700, color: flash === "correct" ? "#34d399" : "#f87171" }}>
          {flash === "correct" ? `✓ சரி! ${streak > 1 ? `🔥 Streak x${streak}` : ""}` : "✗ தவறு!"}
        </div>
      )}
      <div style={S.card}>
        <div style={{ fontSize: "clamp(13px, 3vw, 15px)", fontWeight: 600, color: "#e2e8f0", marginBottom: 16, lineHeight: 1.7, fontFamily: "'Noto Serif Tamil', Georgia, serif" }}>
          {idx + 1}. {q.q}
        </div>
        <div style={{ display: "grid", gridTemplateColumns: "1fr 1fr", gap: 10 }}>
          {q.opts.map((opt, i) => {
            let bg = "rgba(255,255,255,0.04)";
            let border = "1px solid rgba(255,255,255,0.1)";
            let color = "#cbd5e1";
            if (selected !== null) {
              if (i === q.ans) { bg = "rgba(52,211,153,0.2)"; border = "1px solid #34d399"; color = "#34d399"; }
              else if (i === selected && i !== q.ans) { bg = "rgba(248,113,113,0.2)"; border = "1px solid #f87171"; color = "#f87171"; }
            }
            return (
              <button key={i} onClick={() => selected === null && handleAnswer(i)} style={{ background: bg, border, borderRadius: 8, padding: "10px 12px", color, cursor: selected === null ? "pointer" : "default", textAlign: "left", fontSize: "clamp(11px, 2.5vw, 13px)", transition: "all 0.2s", fontFamily: "inherit", lineHeight: 1.5 }}>
                <span style={{ fontWeight: 700, marginRight: 6, opacity: 0.7 }}>{["அ", "ஆ", "இ", "ஈ"][i]})</span>{opt}
              </button>
            );
          })}
        </div>
      </div>
    </div>
  );
}

