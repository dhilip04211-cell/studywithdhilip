// ── Storage Key ────────────────────────────────────────────────────────────────
const STORAGE_KEY = "ccseivgt2025_quiz_state";

// ── Helper ─────────────────────────────────────────────────────────────────────
function loadState() {
  try {
    const raw = localStorage.getItem(STORAGE_KEY);
    return raw ? JSON.parse(raw) : null;
  } catch {
    return null;
  }
}

function saveState(state) {
  try {
    localStorage.setItem(STORAGE_KEY, JSON.stringify(state));
  } catch {}
}

