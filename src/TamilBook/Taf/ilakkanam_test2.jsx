function GameView() {
  const TIMER_MAX = 20;
  const [phase, setPhase] = useState('select');
  const [activeTopic, setActiveTopic] = useState(null);
  const [allQs, setAllQs] = useState([]);
  const [qIdx, setQIdx] = useState(0);
  const [score, setScore] = useState(0);
  const [streak, setStreak] = useState(0);
  const [bestStreak, setBestStreak] = useState(0);
  const [timer, setTimer] = useState(TIMER_MAX);
  const [selected, setSelected] = useState(null);
  const [feedback, setFeedback] = useState(null);
  const [gameOver, setGameOver] = useState(false);
  const [totalCorrect, setTotalCorrect] = useState(0);
  const timerRef = useCallback(null, []);

  useEffect(() => {
    if (phase !== 'game' || selected !== null || gameOver) return;
    const id = setInterval(() => {
      setTimer(t => {
        if (t <= 1) {
          clearInterval(id);
          setGameOver(true);
          return 0;
        }
        return t - 1;
      });
    }, 1000);
    return () => clearInterval(id);
  }, [phase, qIdx, selected, gameOver]);

  const startGame = (topicId) => {
    const shuffled = [...quizData[topicId]].sort(() => Math.random() - 0.5);
    setActiveTopic(topicId);
    setAllQs(shuffled);
    setQIdx(0);
    setScore(0);
    setStreak(0);
    setBestStreak(0);
    setTimer(TIMER_MAX);
    setSelected(null);
    setFeedback(null);
    setGameOver(false);
    setTotalCorrect(0);
    setPhase('game');
  };

  const handleGameOption = (idx) => {
    if (selected !== null || gameOver) return;
    setSelected(idx);
    const q = allQs[qIdx];
    if (idx === q.answer) {
      const newStreak = streak + 1;
      const bonus = newStreak >= 5 ? 20 : newStreak >= 3 ? 15 : 10;
      const timeBonus = Math.floor(timer / 2);
      setScore(s => s + bonus + timeBonus);
      setStreak(newStreak);
      setBestStreak(bs => Math.max(bs, newStreak));
      setTotalCorrect(tc => tc + 1);
      setFeedback('✅');
    } else {
      setStreak(0);
      setFeedback('❌');
    }
    setTimeout(() => {
      setFeedback(null);
      if (qIdx + 1 < allQs.length) {
        setQIdx(qi => qi + 1);
        setSelected(null);
        setTimer(TIMER_MAX);
      } else {
        setGameOver(true);
      }
    }, 1100);
  };

  if (phase === 'select') {
    return (
      <div>
        <div style={{ marginBottom: 16, color: 'var(--text2)', fontSize: '0.95rem' }}>
          வினா விளையாட்டு – நேரப் போட்டி! Streak bonus + Time bonus 🎮
        </div>
        <div className="topic-grid">
          {ALL_TOPICS.map(t => (
            <div key={t} className="topic-card" onClick={() => startGame(t)}>
              <div className="topic-num">{t}</div>
              <div className="topic-title">{notesData[t].title}</div>
              <div className="topic-sub">{quizData[t].length} வினாக்கள் • {TIMER_MAX}s/வினா</div>
            </div>
          ))}
        </div>
      </div>
    );
  }

  if (gameOver) {
    const pct = Math.round((totalCorrect / allQs.length) * 100);
    return (
      <div className="quiz-container">
        <div className="game-over-card">
          <div className="game-over-title">🏁 விளையாட்டு முடிந்தது!</div>
          <div className="game-final-score">{score}</div>
          <div style={{ color: 'var(--text2)', fontFamily: 'Space Mono', fontSize: '0.85rem', marginBottom: 16 }}>மொத்த மதிப்பெண்</div>
          <div className="stats-row">
            <div className="stat-item">
              <div className="stat-val correct">{totalCorrect}</div>
              <div className="stat-lbl">சரியான விடைகள்</div>
            </div>
            <div className="stat-item">
              <div className="stat-val wrong">{allQs.length - totalCorrect}</div>
              <div className="stat-lbl">தவறான விடைகள்</div>
            </div>
            <div className="stat-item">
              <div className="stat-val" style={{ color: 'var(--accent3)' }}>{bestStreak}</div>
              <div className="stat-lbl">சிறந்த streak</div>
            </div>
          </div>
          <div style={{ fontSize: '1.2rem', margin: '12px 0' }}>
            {pct >= 80 ? '🏆 அருமை!' : pct >= 60 ? '👍 நல்லது!' : '💪 மேம்படவும்!'}
          </div>
          <div style={{ display: 'flex', gap: 10, justifyContent: 'center', flexWrap: 'wrap', marginTop: 16 }}>
            <button className="btn btn-primary" onClick={() => startGame(activeTopic)}>மீண்டும் விளையாட</button>
            <button className="btn btn-secondary" onClick={() => setPhase('select')}>தலைப்பு மாற்ற</button>
          </div>
        </div>
      </div>
    );
  }

  const q = allQs[qIdx];
  const labels = ['A', 'B', 'C', 'D'];

  return (
    <div className="quiz-container">
      {feedback && <div className="feedback-flash">{feedback}</div>}
      <div className="game-header">
        <div className="score-badge">⭐ {score}</div>
        <div className={`timer-badge ${timer <= 5 ? 'urgent' : ''}`}>⏱ {timer}s</div>
        <div className="streak-badge">🔥 {streak}</div>
      </div>
      <div style={{ marginBottom: 10 }}>
        <div className="quiz-progress">
          <div className="progress-bar">
            <div className="progress-fill" style={{ width: `${((qIdx + 1) / allQs.length) * 100}%` }} />
          </div>
          <div className="progress-text">{qIdx + 1}/{allQs.length}</div>
        </div>
        <div className="progress-bar" style={{ height: 4, marginTop: 6 }}>
          <div style={{ height: '100%', background: timer <= 5 ? 'var(--wrong)' : 'var(--accent2)', borderRadius: 99, width: `${(timer / TIMER_MAX) * 100}%`, transition: 'width 1s linear' }} />
        </div>
      </div>
      <div className="question-card">
        <div className="question-num">வினா {qIdx + 1}/{allQs.length}</div>
        <div className="question-text">{q.q}</div>
        <div className="options-grid">
          {q.options.map((opt, i) => {
            let cls = 'option-btn';
            if (selected !== null) {
              if (i === q.answer) cls += ' correct';
              else if (i === selected && selected !== q.answer) cls += ' wrong';
              else cls += ' unselected-after';
            }
            return (
              <button key={i} className={cls} onClick={() => handleGameOption(i)} disabled={selected !== null}>
                <span className="option-label">{labels[i]}</span>
                {opt}
              </button>
            );
          })}
        </div>
      </div>
      {streak >= 3 && (
        <div style={{ textAlign: 'center', color: 'var(--accent)', fontFamily: 'Space Mono', fontSize: '0.85rem' }}>
          🔥 {streak} streak — Bonus +{streak >= 5 ? 20 : 15} pts!
        </div>
      )}
    </div>
  );
}

