                  <tr key={ri} style={{ background: ri % 2 === 0 ? "white" : "#f0f7ff" }}>
                    {row.map((cell, ci) => <td key={ci} style={{ padding: "0.45rem 0.6rem", borderBottom: "1px solid #dbeafe", borderRight: "1px solid #dbeafe", color: "#1e293b" }}>{cell}</td>)}
                  </tr>
