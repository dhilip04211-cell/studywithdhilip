// ===================== QUIZ VIEW =====================

function QuizView({ question, total, current, selected, answered, onAnswer, onNext, progress, score }) {
  return (
    <div>
      {/* Progress */}
      <div style={{ marginBottom: 16 }}>
        <div style={{ display: "flex", justifyContent: "space-between", marginBottom: 6, fontSize: 13, color: "#b8a0d0" }}>
          <span>வினா {current + 1} / {total}</span>
          <span>மதிப்பெண்: {score}/{current}</span>
        </div>
        <div style={{ height: 6, background: "rgba(255,255,255,0.1)", borderRadius: 3, overflow: "hidden" }}>
          <div style={{ height: "100%", background: "linear-gradient(90deg, #a070f0, #f0c040)", width: `${progress}%`, transition: "width 0.3s", borderRadius: 3 }} />
        </div>
      </div>

      {/* Topic badge */}
      <div style={{ display: "inline-block", padding: "4px 12px", background: "rgba(160,112,240,0.2)", borderRadius: 12, fontSize: 12, color: "#c8a0f0", marginBottom: 12, border: "1px solid rgba(160,112,240,0.3)" }}>
        {question.topic}
      </div>

      {/* Question */}
      <div style={{ background: "rgba(255,255,255,0.06)", borderRadius: 16, padding: "20px 22px", marginBottom: 16, border: "1px solid rgba(255,255,255,0.1)" }}>
        <p style={{ margin: 0, fontSize: 16, lineHeight: 1.8, color: "#f0eaff", fontWeight: 600 }}>{question.question}</p>
      </div>

      {/* Options */}
      <div style={{ display: "flex", flexDirection: "column", gap: 10, marginBottom: 16 }}>
        {question.options.map((opt, i) => {
          let bg = "rgba(255,255,255,0.05)";
          let border = "1px solid rgba(255,255,255,0.1)";
          let color = "#d0c8e8";
          if (answered) {
            if (i === question.answer) { bg = "rgba(64,200,64,0.15)"; border = "1px solid #40c840"; color = "#80f080"; }
            else if (i === selected && i !== question.answer) { bg = "rgba(200,64,64,0.15)"; border = "1px solid #c84040"; color = "#f08080"; }
          } else if (selected === i) {
            bg = "rgba(160,112,240,0.2)"; border = "1px solid #a070f0"; color = "#c8a0f0";
          }
          return (
            <button key={i} onClick={() => onAnswer(i)} disabled={answered} style={{ padding: "12px 16px", borderRadius: 12, border, background: bg, color, cursor: answered ? "default" : "pointer", textAlign: "left", fontFamily: "inherit", fontSize: 14, lineHeight: 1.6, transition: "all 0.2s", display: "flex", alignItems: "flex-start", gap: 10 }}>
              <span style={{ fontWeight: 700, minWidth: 22, color: answered && i === question.answer ? "#80f080" : answered && i === selected ? "#f08080" : "#a070f0" }}>{["A", "B", "C", "D"][i]}.</span>
              <span>{opt}</span>
              {answered && i === question.answer && <span style={{ marginLeft: "auto" }}>✅</span>}
              {answered && i === selected && i !== question.answer && <span style={{ marginLeft: "auto" }}>❌</span>}
            </button>
          );
        })}
      </div>

      {/* Explanation */}
      {answered && (
        <div style={{ background: "rgba(240,192,64,0.08)", border: "1px solid rgba(240,192,64,0.25)", borderRadius: 12, padding: "14px 16px", marginBottom: 16 }}>
          <p style={{ margin: 0, fontSize: 13, lineHeight: 1.7 }}>
            <span style={{ color: "#f0c040", fontWeight: 700 }}>💡 விளக்கம்: </span>
            <span style={{ color: "#d8c880" }}>{question.explanation}</span>
          </p>
        </div>
      )}

      {answered && (
        <button onClick={onNext} style={{ width: "100%", padding: "14px", borderRadius: 14, border: "none", cursor: "pointer", fontFamily: "inherit", fontSize: 15, fontWeight: 700, background: "linear-gradient(135deg, #a070f0, #f0c040)", color: "#1a1040" }}>
          {current + 1 >= questions.length ? "📊 முடிவு காண்க" : "அடுத்த வினா →"}
        </button>
      )}
    </div>
  );
}

