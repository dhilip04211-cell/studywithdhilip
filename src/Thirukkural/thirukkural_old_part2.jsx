// Build quiz - store answer VALUE not index to avoid shuffle bugs
// ============================================================
function buildQuizQuestions() {
  const qs = [];

  // General facts
  generalFacts.forEach(f => {
    qs.push({ ...f, chapter: "பொது", type: "general" });
  });

  // Per-chapter questions
  chapters.forEach(ch => {
    // Chapter pal question
    qs.push({
      chapter: ch.title,
      type: "chapter",
      q: `"${ch.title}" அதிகாரம் எந்த பாலில் வருகிறது?`,
      opts: ["அறத்துப்பால்", "பொருட்பால்", "காமத்துப்பால்", "எந்த பாலிலும் இல்லை"],
      ans: ch.pal,
      exp: `"${ch.title}" அதிகாரம் ${ch.pal}ல் வருகிறது. ${ch.subtitle}.`
    });

    ch.kurals.forEach(k => {
      const firstLine = k.kural.split("\n")[0];

      // பொருள் question
      if (k.porul) {
        const wrongPoruls = ch.kurals
          .filter(x => x.num !== k.num && x.porul)
          .map(x => x.porul.substring(0, 60));
        const opts = buildMCQ(k.porul, wrongPoruls);
        qs.push({
          chapter: ch.title,
          type: "porul",
          q: `"${firstLine}" – இக்குறளின் பொருள் என்ன?`,
          opts,
          ans: k.porul,
          exp: k.porul
        });
      }

      // சொற்பொருள் questions
      k.sorporul.forEach(sp => {
        const wrongPool = chapters.flatMap(c => c.kurals.flatMap(ku =>
          ku.sorporul.filter(s => s.porul !== sp.porul).map(s => s.porul)
        ));
        const opts = buildMCQ(sp.porul, wrongPool);
        qs.push({
          chapter: ch.title,
          type: "sorporul",
          q: `"${sp.sol}" என்ற சொல்லின் பொருள் என்ன? (${ch.title} – குறள் ${k.num})`,
          opts,
          ans: sp.porul,
          exp: `"${sp.sol}" என்பதன் பொருள்: ${sp.porul} (${ch.title}, குறள் ${k.num})`
        });
      });

      // இலக்கணம் questions – ask about specific word
      k.ilakkanam.forEach(il => {
        if (il.sol && il.vidhivuri && il.vidhivuri.length > 3) {
          const wrongPool = [
            "வினைத்தொகை", "பண்புத்தொகை", "உவமையணி", "வேற்றுமைத்தொகை",
            "தொழிற்பெயர்", "வினையாலணியும் பெயர்", "பெயரெச்சம்", "வினையெச்சம்",
            "வியங்கோள் வினைமுற்று", "எண்ணும்மை", "பலர்பால் வினைமுற்று",
            "ஈறுகெட்ட எதிர்மறைப்பெயரெச்சம்", "குறிப்பு வினைமுற்று",
            "இழிவுச்சிறப்பும்மை", "உரிச்சொற்றொடர்", "அசைச்சொல்"
          ].filter(x => x !== il.vidhivuri);
          const opts = buildMCQ(il.vidhivuri, wrongPool);
          qs.push({
            chapter: ch.title,
            type: "ilakkanam",
            q: `"${il.sol}" என்னும் சொல்லின் இலக்கண இனம் என்ன? (${ch.title} – குறள் ${k.num})`,
            opts,
            ans: il.vidhivuri,
            exp: `"${il.sol}" – ${il.vidhivuri} (${ch.title}, குறள் ${k.num})`
          });
        }
      });

      // அதிகாரம் identify
      const otherChTitles = chapters.filter(c => c.id !== ch.id).map(c => c.title);
      const opts2 = buildMCQ(ch.title, otherChTitles);
      qs.push({
        chapter: ch.title,
        type: "identify",
        q: `"${firstLine}" – இக்குறள் எந்த அதிகாரத்தில் உள்ளது?`,
        opts: opts2,
        ans: ch.title,
        exp: `இக்குறள் "${ch.title}" அதிகாரத்தில் உள்ளது. ${ch.subtitle}.`
      });
    });
  });

  return shuffleArray(qs).map((q, i) => ({ ...q, id: i }));
}

// buildMCQ returns shuffled options; correct answer stored as value (ans), not index
function buildMCQ(correct, pool) {
  const unique = [...new Set(pool.filter(x => x && x !== correct))];
  const wrongs = shuffleArray(unique).slice(0, 3);
  return shuffleArray([correct, ...wrongs]);
}

function shuffleArray(arr) {
  const a = [...arr];
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

