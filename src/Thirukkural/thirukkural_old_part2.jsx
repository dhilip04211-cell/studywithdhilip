export default function thirukkural_old_part2() {
  const [mode, setMode] = useState("home");
  const [notesChapter, setNotesChapter] = useState(0);
  const [quizQs, setQuizQs] = useState([]);
  const [quizIdx, setQuizIdx] = useState(0);
  const [selected, setSelected] = useState(null); // stores selected option value
  const [score, setScore] = useState(0);
  const [answered, setAnswered] = useState({});
  const [showResult, setShowResult] = useState(false);
  const [filter, setFilter] = useState("all");
  const [loaded, setLoaded] = useState(false);
  const [savedResults, setSavedResults] = useState([]);
  const [expandedKural, setExpandedKural] = useState(null);

  useEffect(() => {
    async function init() {
      const qq = buildQuizQuestions();
      setQuizQs(qq);
      const np = await loadData(NOTES_KEY);
      if (np) setNotesChapter(np.chapter || 0);
      const qp = await loadData(QUIZ_KEY);
      if (qp) { setQuizIdx(qp.index || 0); setScore(qp.score || 0); setAnswered(qp.answered || {}); }
      const res = await loadData(RESULTS_KEY);
      if (res) setSavedResults(res);
      setLoaded(true);
    }
    init();
  }, []);

  useEffect(() => { if (loaded) saveData(NOTES_KEY, { chapter: notesChapter }); }, [notesChapter, loaded]);
  useEffect(() => { if (loaded && quizQs.length) saveData(QUIZ_KEY, { index: quizIdx, score, answered }); }, [quizIdx, score, answered, loaded]);

  const filteredQ = quizQs.filter(q => filter === "all" || q.type === filter || q.chapter === filter);

  function handleAnswer(optVal) {
    if (selected !== null) return;
    const q = filteredQ[quizIdx];
    if (!q) return;
    setSelected(optVal);
    const correct = optVal === q.ans;
    setAnswered(prev => ({ ...prev, [quizIdx]: { selected: optVal, correct } }));
    if (correct) setScore(s => s + 1);
  }

  function nextQ() {
    if (quizIdx < filteredQ.length - 1) {
      setQuizIdx(i => i + 1);
      setSelected(null);
    } else {
      setShowResult(true);
      const pct = Math.round((score / filteredQ.length) * 100);
      const newResult = { date: new Date().toLocaleDateString("ta-IN"), score, total: filteredQ.length, pct };
      const updated = [newResult, ...savedResults].slice(0, 5);
      setSavedResults(updated);
      saveData(RESULTS_KEY, updated);
    }
  }

  function resetQuiz() {
    const qq = buildQuizQuestions();
    setQuizQs(qq);
    setQuizIdx(0); setSelected(null); setScore(0); setAnswered({});
    setShowResult(false);
    saveData(QUIZ_KEY, { index: 0, score: 0, answered: {} });
  }

  if (!loaded) return (
    <div style={S.loading}>
      <div style={S.loadingIcon}>📖</div>
      <div style={S.loadingText}>திருக்குறள் ஏற்றுகிறது…</div>
    </div>
  );

  if (mode === "home") return <HomeScreen setMode={setMode} savedResults={savedResults} chapters={chapters} quizQs={quizQs} />;
  if (mode === "notes") return <NotesScreen chapters={chapters} notesChapter={notesChapter} setNotesChapter={setNotesChapter} setMode={setMode} expandedKural={expandedKural} setExpandedKural={setExpandedKural} />;
  if (mode === "quiz") return <QuizScreen filteredQ={filteredQ} quizIdx={quizIdx} selected={selected} score={score} filter={filter} setFilter={setFilter} showResult={showResult} handleAnswer={handleAnswer} nextQ={nextQ} resetQuiz={resetQuiz} setMode={setMode} chapters={chapters} />;
  return null;
}

