// QUIZ SCREEN
// ============================================================
function QuizScreen({ filteredQ, quizIdx, selected, score, filter, setFilter, showResult, handleAnswer, nextQ, resetQuiz, setMode, chapters }) {
  const typeLabels = { all: "அனைத்தும்", porul: "பொருள்", sorporul: "சொற்பொருள்", ilakkanam: "இலக்கணம்", identify: "அதிகாரம்", chapter: "பால்", general: "பொது" };
  const chapterFilters = chapters.map(c => c.title);

  if (showResult) {
    const pct = filteredQ.length > 0 ? Math.round((score / filteredQ.length) * 100) : 0;
    const grade = pct >= 90 ? "சிறப்பு! 🏆" : pct >= 70 ? "நலம்! 🌟" : pct >= 50 ? "முயற்சி தேவை 📖" : "மீண்டும் படியுங்கள் 💪";
    return (
      <div style={S.screen}>
        <div style={S.resultScreen}>
          <div style={S.resultIcon}>{pct >= 70 ? "🎉" : "📖"}</div>
          <h2 style={S.resultTitle}>முடிவு</h2>
          <div style={S.resultScore}>{score}<span style={{ fontSize: 24, color: "#888" }}>/{filteredQ.length}</span></div>
          <div style={S.resultPct}>{pct}%</div>
          <div style={S.resultGrade}>{grade}</div>
          <div style={S.resultBtns}>
            <button style={S.resultBtn} onClick={resetQuiz}>🔄 மீண்டும்</button>
            <button style={{ ...S.resultBtn, background: "#333", color: "#fff" }} onClick={() => setMode("home")}>🏠 முகப்பு</button>
          </div>
        </div>
      </div>
    );
  }

  const q = filteredQ[quizIdx];
  if (!q) return (
    <div style={S.screen}>
      <div style={{ padding: 40, textAlign: "center", color: "#ffd700" }}>
        <p>கேள்விகள் இல்லை. வடிகட்டியை மாற்றவும்.</p>
        <button style={S.resultBtn} onClick={() => setMode("home")}>முகப்பு</button>
      </div>
    </div>
  );

  const isAnswered = selected !== null;
  const progress = filteredQ.length > 0 ? ((quizIdx + 1) / filteredQ.length) * 100 : 0;

  return (
    <div style={S.screen}>
      <div style={S.quizHeader}>
        <button style={S.backBtn} onClick={() => setMode("home")}>← முகப்பு</button>
        <div style={S.quizProgress}>
          <span style={S.qNum}>{quizIdx + 1} / {filteredQ.length}</span>
          <span style={S.qScore}>மதிப்பெண்: {score}</span>
        </div>
      </div>

      {/* Filter row */}
      <div style={S.filterRow}>
        {Object.entries(typeLabels).map(([k, v]) => (
          <button key={k} style={{ ...S.filterBtn, ...(filter === k ? S.filterBtnActive : {}) }}
            onClick={() => setFilter(k)}>{v}</button>
        ))}
        {chapterFilters.map(cf => (
          <button key={cf} style={{ ...S.filterBtn, ...(filter === cf ? S.filterBtnActive : {}) }}
            onClick={() => setFilter(cf)}>{cf}</button>
        ))}
      </div>

      <div style={S.progressBar}>
        <div style={{ ...S.progressFill, width: `${progress}%` }} />
      </div>

      <div style={S.questionCard}>
        <div style={S.chapterTag}>📚 {q.chapter} | {typeLabels[q.type] || q.type}</div>
        <div style={S.questionText}>{q.q}</div>

        <div style={S.optionsList}>
          {q.opts.map((opt, i) => {
            const isSelected = selected === opt;
            const isCorrect = opt === q.ans;
            let style = S.option;
            if (isAnswered) {
              if (isCorrect) style = { ...S.option, ...S.optCorrect };
              else if (isSelected && !isCorrect) style = { ...S.option, ...S.optWrong };
            } else if (isSelected) {
              style = { ...S.option, ...S.optSelected };
            }
            return (
              <button key={i} style={style} onClick={() => handleAnswer(opt)} disabled={isAnswered && !isSelected && !isCorrect}>
                <span style={S.optLabel}>{["அ","ஆ","இ","ஈ"][i]}</span>
                <span style={S.optText}>{opt}</span>
                {isAnswered && isCorrect && <span style={S.tick}>✓</span>}
                {isAnswered && isSelected && !isCorrect && <span style={S.cross}>✗</span>}
              </button>
            );
          })}
        </div>

        {isAnswered && (
          <div style={S.explanation}>
            <span style={S.expLabel}>{selected === q.ans ? "✅ சரி!" : "❌ தவறு"}</span>
            <p style={S.expText}>{q.exp}</p>
          </div>
        )}
      </div>

      {isAnswered && (
        <button style={S.nextBtn} onClick={nextQ}>
          {quizIdx < filteredQ.length - 1 ? "அடுத்த கேள்வி →" : "முடிவு காண"}
        </button>
      )}

      <div style={S.jumpRow}>
        {quizIdx > 0 && <button style={S.jumpBtn} onClick={() => { /* reset handled by parent */ }}>← முந்தைய</button>}
        <button style={{ ...S.jumpBtn, flex: 2 }} onClick={resetQuiz}>🔄 மீட்டமை</button>
      </div>
    </div>
  );
}

