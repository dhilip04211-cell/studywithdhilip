// Generate quiz questions from all kurals
function generateQuizQuestions() {
  const questions = [];
  CHAPTERS.forEach(chapter => {
    chapter.kurals.forEach(kural => {
      // Q1: Meaning
      questions.push({
        id: `${kural.no}_meaning`,
        chapter: chapter.title,
        chapterEn: chapter.titleEn,
        kuralNo: kural.no,
        kuralText: kural.kural,
        question: `குறள் ${kural.no}: "${kural.kural[0]}" — இதன் பொருள் என்ன?`,
        answer: kural.meaning,
        type: "meaning",
        options: null
      });
      // Q2: First line identify
      questions.push({
        id: `${kural.no}_identify`,
        chapter: chapter.title,
        chapterEn: chapter.titleEn,
        kuralNo: kural.no,
        kuralText: kural.kural,
        question: `"${kural.kural[0]}" — இந்த குறள் எந்த அதிகாரத்தில் உள்ளது?`,
        answer: chapter.title,
        type: "chapter",
        options: null
      });
    });
  });
  return questions;
}

